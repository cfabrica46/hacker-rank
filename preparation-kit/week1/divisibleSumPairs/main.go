func divisibleSumPairs(n int32, k int32, ar []int32) int32 {
	var out int32

	for i := 0; i < int(n); i++ {
		for j := i + 1; j < int(n); j++ {
			if (ar[i]+ar[j])%k == 0 {
				out++
			}
		}
	}
	return out
}
